import random

# Определим возможные действия игроков
ACTIONS = ['Олень', 'Заяц']

# Определим матрицу выигрышей
PAYOFF_MATRIX = {
    ('Олень', 'Олень'): (4, 4),
    ('Олень', 'Заяц'): (0, 3),
    ('Заяц', 'Олень'): (3, 0),
    ('Заяц', 'Заяц'): (3, 3),
}

# Стратегия игрока 1: всегда выбирать охоту на оленя
def player1_strategy():
    return 'Олень'

# Стратегия игрока 2: случайный выбор
def player2_strategy():
    return random.choice(ACTIONS)

# Функция для симуляции одного раунда игры
def play_round(player1_strategy, player2_strategy):
    action1 = player1_strategy()
    action2 = player2_strategy()
    payoff = PAYOFF_MATRIX[(action1, action2)]
    return action1, action2, payoff

# Функция для симуляции нескольких раундов игры
def simulate_game(player1_strategy, player2_strategy, rounds=100):
    total_payoff1 = 0
    total_payoff2 = 0
    
    for _ in range(rounds):
        action1, action2, payoff = play_round(player1_strategy, player2_strategy)
        total_payoff1 += payoff[0]
        total_payoff2 += payoff[1]
    
    return total_payoff1, total_payoff2

# Запуск симуляции
total_payoff1, total_payoff2 = simulate_game(player1_strategy, player2_strategy, rounds=1000)
print(f"Игрок 1: {total_payoff1} очков")
print(f"Игрок 2: {total_payoff2} очков")

